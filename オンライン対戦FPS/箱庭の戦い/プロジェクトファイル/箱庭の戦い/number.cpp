// ポリゴンの描画処理
//=============================================================================
void CNumber::Draw(void)
{
	CScene2D::Draw();
}

//=============================================================================
// ゲッダー
//=============================================================================
CNumber *CNumber::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, string Type)
{
	CNumber *pNumber = NULL;
	if (pNumber == NULL)
	{
		pNumber = new CNumber;
	}

	pNumber->m_Pos = pos;
	pNumber->m_Size = size;
	pNumber->m_Type = Type;

	if (pNumber != NULL)
	{
		pNumber->Init();
	}
	return pNumber;
}